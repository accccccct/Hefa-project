package com.hefa.user.pojo.vo;

public class RegionUserInfo {
	private String regionUserName;//售后专员名称
	private String regionUserDept;//售后专员部门
	private String regionArea;//负责区域
	private String createTime;//创建时间
	private String regionUserCode;//售后专员编码
	public String getRegionUserName() {
		return regionUserName;
	}
	public void setRegionUserName(String regionUserName) {
		this.regionUserName = regionUserName;
	}
	public String getRegionUserDept() {
		return regionUserDept;
	}
	public void setRegionUserDept(String regionUserDept) {
		this.regionUserDept = regionUserDept;
	}
	public String getRegionArea() {
		return regionArea;
	}
	public void setRegionArea(String regionArea) {
		this.regionArea = regionArea;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	public String getRegionUserCode() {
		return regionUserCode;
	}
	public void setRegionUserCode(String regionUserCode) {
		this.regionUserCode = regionUserCode;
	}
	
	
}
